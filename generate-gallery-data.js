const fs = require("fs");
const path = require("path");

const root = __dirname;
const categories = {
  bestMoments: "images/moments",
  ceremony: "images/ceremony",
  family: "images/family",
  reception: "images/reception",
  silentHeroes: "images/heroes",
};
const imageExtensions = [".webp", ".jpg", ".jpeg", ".png"];

function getFiles(folder) {
  const dir = path.join(root, folder);

  if (!fs.existsSync(dir)) {
    return [];
  }

  return fs
    .readdirSync(dir)
    .filter((file) =>
      imageExtensions.includes(path.extname(file).toLowerCase()),
    )
    .sort((a, b) =>
      a.localeCompare(b, undefined, { numeric: true, sensitivity: "base" }),
    )
    .map((file) => ({
      src: `${folder.replace(/\\/g, "/")}/${file}`,
      alt: createAltText(folder, file),
    }));
}

function createAltText(folder, file) {
  const name = path.basename(file, path.extname(file));
  const title = name
    .replace(/[-_]+/g, " ")
    .replace(/(\d+)/g, " $1")
    .replace(/\s+/g, " ")
    .trim();
  const category = folder.split("/")[1];

  switch (category) {
    case "moments":
      return `Best wedding moment ${title}`;
    case "ceremony":
      return `Wedding ceremony ${title}`;
    case "family":
      return `Family portrait ${title}`;
    case "reception":
      return `Wedding reception ${title}`;
    case "heroes":
      return `Silent hero ${title}`;
    case "silent-heroes":
      return `Silent hero ${title}`;
    case "silent-heroes":
      return `Silent hero ${title}`;
    default:
      return title;
  }
}

function generateData() {
  const data = {};
  for (const [key, folder] of Object.entries(categories)) {
    data[key] = getFiles(folder);
  }

  const output = `// This file is generated by generate-gallery-data.js\nwindow.galleryData = ${JSON.stringify(data, null, 2)};\n`;
  fs.writeFileSync(path.join(root, "gallery-data.js"), output, "utf8");

  const total = Object.values(data).reduce((sum, arr) => sum + arr.length, 0);
  console.log(
    `[${new Date().toLocaleTimeString()}] gallery-data.js updated with ${total} images.`,
  );
}

// Initial generation
generateData();

// Watch for file changes
let watchTimeout;
const watchDirs = Object.values(categories).map((folder) =>
  path.join(root, folder),
);

watchDirs.forEach((dir) => {
  if (fs.existsSync(dir)) {
    fs.watch(dir, (eventType, filename) => {
      if (
        filename &&
        imageExtensions.includes(path.extname(filename).toLowerCase())
      ) {
        // Debounce: wait 500ms for file operations to complete
        clearTimeout(watchTimeout);
        watchTimeout = setTimeout(() => {
          console.log(`\n📸 Detected ${eventType}: ${filename}`);
          generateData();
        }, 500);
      }
    });
  }
});

console.log("👀 Watching for image changes in:");
watchDirs.forEach((dir) => console.log(`   ${dir}`));
console.log("Press Ctrl+C to stop.\n");
